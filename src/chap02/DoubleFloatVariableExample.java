package chap02;

public class DoubleFloatVariableExample {
    public static void main(String[] args) {
        double d1 = 3.14;
        float d2 = 3.14f;

        double d3 = 0.123456789123456789;
        float d4 = 0.123456789123456789f;

        System.out.println(d3);
        System.out.println(d4);
    }
}
